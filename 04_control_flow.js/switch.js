// switch statement is also like if else statement, just this is another form of condition checking code, if any case will be true then all case will be ecape but all case must have break keyword.

const month = "march"

switch (month) {
    case "jan":
        console.log("January");
        break;
    case "feb":
        console.log("feb");
        break;
    case "march":
        console.log("march");
        break;
    case "april":
        console.log("april");
        break;

    default:
        console.log("default case match");
        break;
}